    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed
        try{
            String nombres, apellidos, sexo, residencia, centro;
            int codigo;
            nombres = input_nombres.getText();
            apellidos = input_apellidos.getText();
            codigo = Integer.parseInt(input_codigo.getText());
            sexo = input_sexo.getText();
            residencia = input_residencia.getText();
            centro = input_centro.getText();

            if(!"".equals(nombres)&&!"".equals(apellidos)&&!"".equals(codigo)&&!"".equals(sexo)&&!"".equals(residencia)){
                Medicos medico_aux = new Medicos();
                medico_aux.setNombres(nombres);
                medico_aux.setApellidos(apellidos);
                medico_aux.setCodigo(codigo);
                medico_aux.setSexo(sexo);
                medico_aux.setDireccion_residencia(residencia);
                medico_aux.setCentro(centro);
                lista_medicos1.add(medico_aux);
            }

        }catch(Exception e){}
        mostrarTabla();
        frame_interno_agregar_medico.setVisible(false);
        input_nombres.setText("");
        input_apellidos.setText("");
        input_codigo.setText("");
        input_sexo.setText("");
        input_residencia.setText("");
    }//GEN-LAST:event_btn_enviarActionPerformed

