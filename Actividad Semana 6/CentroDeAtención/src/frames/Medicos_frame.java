    public void mostrarTabla(){
        String matriz[][] = new String[lista_medicos1.size()][10];
        for(int i=0; i<lista_medicos1.size();i++){
            matriz[i][0]=lista_medicos1.get(i).getCodigoString();
            matriz[i][1]=lista_medicos1.get(i).getNombres();
            matriz[i][2]=lista_medicos1.get(i).getApellidos();
            matriz[i][3]=lista_medicos1.get(i).getSexo();
            matriz[i][4]=lista_medicos1.get(i).getDireccion_residencia();
            matriz[i][5]=lista_medicos1.get(i).getCentro();

        }
        listado_pacientes.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Código", "Nombres", "Apellidos", "Sexo", "Dirección", "Centro"
            } 
            
        ){
            
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            };
            
        });
 
    }
